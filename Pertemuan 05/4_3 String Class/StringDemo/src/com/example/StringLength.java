public class StringLength {
    public static void main (String[] args){
        // method length untuk menentukan banyaknya karakter dalam string
        String nama = "Metta Liyanti";
        System.out.println(nama.length()); // 13 termasuk spasi
        
        String angka = "1546372867956857000586";
        System.out.println(angka.length()); //22
    }
}
