package com.example;

public class BooleanIfDriving {
    public static void main(String args[]) {
        String left = "museum";
        String straight = "gym";
        String right = "restaurant";
      
        if(left=="gym"){
             System.out.println("Turn Left");
        }
        
        if(straight=="gym"){
             System.out.println("Drive straight");
        }

        if(right=="gym"){
             System.out.println("Turn Right");
        }
    }

    
}
